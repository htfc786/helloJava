package com.htfc786.yunsuanfudemo;

public class PlusPlusDemo {
    public static void main(String[] args) {
        int a = 6;
        System.out.println(a);  //6
        System.out.println(a++);//6
        System.out.println(a);  //7
        System.out.println(++a);//8
        System.out.println(a);  //8
        System.out.println(a--);//8
        System.out.println(a);  //7
        System.out.println(--a);//6
        System.out.println(a);  //6
    }
}
